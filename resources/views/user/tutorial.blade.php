@section('js')
<script>
  function videoPlay(index, clickedOnButton){
     if (clickedOnButton==true){
      $('#video-control-'+index).trigger('play');
     }    
     $('.video-icon-play-'+index).hide();
  }
  function videoPause(index) {
    $('#video-control-'+index).trigger('pause');
  }

  $('a[data-toggle="tab"]').on('hidden.bs.tab', function (e) {
    var target = $(e.target).attr("href") // activated tab
    var index = target.replace('#video','');
    videoPause(index);
  });

</script>
